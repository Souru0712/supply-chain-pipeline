FRED_BASE_URL = "https://api.stlouisfed.org/fred/series/observations"
REQUEST_TIMEOUT = 10  # seconds applied to every requests.get() call

MANIFEST_REQUIRED_KEYS = {"source", "rows", "timestamp", "status", "output_path"}


_FREQ_MAP = {
    "A": "annual",
    "SA": "semiannual",
    "Q": "quarterly",
    "M": "monthly",
    "BW": "biweekly",
    "W": "weekly",
    "D": "daily",
}


def _request_with_retry(
    url: str, params: dict, max_retries: int = 5
) -> requests.Response:
    """
    Send a GET request with exponential backoff on 429 / 400 responses.

    FRED's free-tier rate limit is ~120 requests per minute.  When the limit
    is hit the API returns 429 (Too Many Requests) or sometimes 400
    (Bad Request).  This helper retries with increasing delays before
    giving up.
    """
    for attempt in range(max_retries):
        resp = requests.get(url, params=params, timeout=REQUEST_TIMEOUT)
        if resp.status_code in (429, 400):
            wait = 2**attempt  # 1 s, 2 s, 4 s, 8 s, 16 s
            print(
                f"    -> HTTP {resp.status_code} -- backing off {wait}s "
                f"(attempt {attempt + 1}/{max_retries})"
            )
            time.sleep(wait)
            continue
        resp.raise_for_status()
        return resp
    # All retries exhausted â€” raise the last status so the caller can skip
    resp.raise_for_status()
    return resp  # unreachable, but keeps linters happy


# Throttle between series: stay well under 120 req/min (2 requests per series)
REQUEST_DELAY = 1.0  # seconds between series fetches


def fetch_series(series_id: str) -> pd.DataFrame:
    """
    Fetch observations for a FRED series ID starting 2015-01-01.

    Also calls the FRED /series endpoint to get the official frequency label.

    Returns an empty DataFrame on any network/HTTP error (caller checks for
    emptiness and raises RuntimeError if needed).

    Columns returned on success:
      date      â€” observation date (YYYY-MM-DD)
      ppi       â€” raw reported index value from FRED
      frequency â€” annual / quarterly / monthly / weekly / etc.
    """
    obs_params = {
        "series_id": series_id,
        "api_key": FRED_API_KEY,
        "file_type": "json",
        "observation_start": "2015-01-01",
    }
    info_params = {
        "series_id": series_id,
        "api_key": FRED_API_KEY,
        "file_type": "json",
    }

    # ------------------------------------------------------------------ #
    # HTTP requests (with retry on rate-limit errors)                      #
    # ------------------------------------------------------------------ #
    try:
        obs_resp = _request_with_retry(FRED_BASE_URL, obs_params)
        info_resp = _request_with_retry(
            "https://api.stlouisfed.org/fred/series",
            info_params,
        )

    except requests.exceptions.Timeout:
        print(
            f"  [TIMEOUT] Series {series_id} did not respond within "
            f"{REQUEST_TIMEOUT}s â€” skipping."
        )
        return pd.DataFrame()

    except requests.exceptions.HTTPError as exc:
        print(
            f"  [HTTP ERROR] Series {series_id} returned "
            f"HTTP {exc.response.status_code}: {exc} â€” skipping."
        )
        return pd.DataFrame()

    except Exception as exc:
        print(
            f"  [ERROR] Unexpected error fetching series {series_id}: "
            f"{type(exc).__name__}: {exc} â€” skipping."
        )
        return pd.DataFrame()

    # ------------------------------------------------------------------ #
    # Parse frequency from series metadata                                 #
    # ------------------------------------------------------------------ #
    serieses = info_resp.json().get("seriess", [])
    freq_short = serieses[0].get("frequency_short", "") if serieses else ""
    frequency = _FREQ_MAP.get(freq_short, freq_short.lower() or "unknown")

    # ------------------------------------------------------------------ #
    # Parse observations                                                   #
    # ------------------------------------------------------------------ #
    observations = obs_resp.json().get("observations", [])

    if not observations:
        return pd.DataFrame()

    df = pd.DataFrame(observations)[["date", "value"]]
    df.columns = ["date", "ppi"]

    # Drop FRED null sentinel "."
    df = df[df["ppi"] != "."].copy()
    df["ppi"] = df["ppi"].astype(float)

    if df.empty:
        raise RuntimeError(
            f"Series {series_id}: fetch succeeded but returned "
            "zero usable observations. Check the series ID and date range."
        )

    df["frequency"] = frequency
    return df


def write_manifest(manifest: dict, timestamp: str) -> str:
    """
    Write a run manifest dict to data/raw/manifest_{timestamp}.json.

    Returns the path of the written file so callers can log or print it.
    Raises ValueError immediately if any of the five required keys are absent,
    so a malformed manifest is caught before it ever touches disk.
    """
    missing = MANIFEST_REQUIRED_KEYS - manifest.keys()
    if missing:
        raise ValueError(
            f"Manifest is missing required keys: {missing}. "
            "All five keys (source, rows, timestamp, status, output_path) must be present."
        )

    manifest_path = f"logs/manifest_{timestamp}.json"
    with open(manifest_path, "w", encoding="utf-8") as fh:
        json.dump(manifest, fh, indent=2)

    return manifest_path


def validate_manifest(manifest_path: str) -> None:
    """
    Re-open the manifest file from disk and assert all five required keys
    are present and non-empty. Raises ValueError on any violation so the
    caller sees an explicit, actionable error message.
    """
    with open(manifest_path, "r", encoding="utf-8") as fh:
        loaded = json.load(fh)

    missing = MANIFEST_REQUIRED_KEYS - loaded.keys()
    if missing:
        raise ValueError(
            f"Manifest validation FAILED â€” missing keys: {missing}\n"
            f"File: {manifest_path}"
        )

    empty_keys = [k for k in MANIFEST_REQUIRED_KEYS if not loaded[k] and loaded[k] != 0]
    if empty_keys:
        raise ValueError(
            f"Manifest validation FAILED â€” keys present but empty/null: {empty_keys}\n"
            f"File: {manifest_path}"
        )

    print("\nâœ“ Manifest validated â€” all five keys present and non-empty:")
    for key in sorted(MANIFEST_REQUIRED_KEYS):
        print(f"    {key}: {loaded[key]!r}")


def main():
    all_frames = []
    failed_series = []

    total_series = len(SERIES_MAP)
    for idx, (series_id, info) in enumerate(SERIES_MAP.items(), 1):
        ingredient_ids = info["ingredient_ids"]
        names = info["names"]
        combined_name = " | ".join(names)
        print(
            f"[{idx}/{total_series}] Fetching {series_id} ({len(ingredient_ids)} ingredients) ..."
        )
        try:
            df = fetch_series(series_id)

            # fetch_series returns an empty DataFrame on network errors
            # (it already printed a message); skip without re-raising here.
            if df.empty:
                failed_series.append((series_id, ingredient_ids, "empty response"))
                logger.warning(
                    "FETCH_FAILED  series=%s  ingredient_ids=%s  error_type=EmptyResponse",
                    series_id,
                    ingredient_ids,
                )
                continue

            # Fan out: one copy of the PPI data per ingredient_id
            for iid in ingredient_ids:
                idf = df.copy()
                idf["ingredient_id"] = iid
                idf["fred_series_id"] = series_id
                idf["ingredient_list_of_name"] = combined_name
                all_frames.append(idf)

            print(
                f"  {len(df)} observations loaded for {len(ingredient_ids)} ingredients."
            )

        except RuntimeError as exc:
            print(f"  [SKIPPED] {exc}")
            failed_series.append((series_id, ingredient_ids, str(exc)))
            logger.warning(
                "FETCH_FAILED  series=%s  ingredient_ids=%s  error_type=RuntimeError",
                series_id,
                ingredient_ids,
            )

        except Exception as exc:
            print(
                f"  [ERROR] Unexpected error processing {series_id}: "
                f"{type(exc).__name__}: {exc}"
            )
            failed_series.append((series_id, ingredient_ids, str(exc)))
            logger.warning(
                "FETCH_FAILED  series=%s  ingredient_ids=%s  error_type=%s",
                series_id,
                ingredient_ids,
                type(exc).__name__,
            )

        # Throttle: pause between series to stay under FRED rate limit
        if idx < total_series:
            time.sleep(REQUEST_DELAY)

    if not all_frames:
        raise RuntimeError(
            "No data was collected â€” all series failed. "
            "Check your FRED_API_KEY and network connectivity."
        )

    combined = pd.concat(all_frames, ignore_index=True)

    combined = combined[
        [
            "ingredient_id",
            "fred_series_id",
            "ingredient_name",
            "ppi",
            "date",
            "frequency",
        ]
    ]

    timestamp = datetime.utcnow().strftime("%Y%m%d_%H%M%S")
    output_path = f"data/raw/macroeconomic/commodity_prices_{timestamp}.csv"

    os.makedirs("data/raw/macroeconomic", exist_ok=True)
    combined.to_csv(output_path, index=False)
    print(f"\nWrote {len(combined)} rows â†’ {output_path}")

    logger.info(
        "FILE_WRITTEN  source=FRED_commodity_prices  rows=%d  path=%s",
        len(combined),
        output_path,
    )

    manifest = {
        "source": "FRED_commodity_prices",
        "rows": len(combined),
        "timestamp": timestamp,
        "status": "success",
        "output_path": output_path,
    }

    manifest_path = write_manifest(manifest, timestamp)
    print(f"Wrote manifest â†’ {manifest_path}")

    logger.info(
        "MANIFEST_WRITTEN  source=FRED_commodity_prices  path=%s",
        manifest_path,
    )

    validate_manifest(manifest_path)

    print(
        f"\nSummary: {combined['ingredient_id'].nunique()} ingredients, "
        f"{combined['fred_series_id'].nunique()} unique series, "
        f"{len(combined)} total observations."
    )

    if failed_series:
        print(f"\nâš   {len(failed_series)} series failed and were skipped:")
        for sid, iids, reason in failed_series:
            print(f"  {sid:25s} ({len(iids)} ingredients) â€” {reason}")


if __name__ == "__main__":
    main()
